package br.com.tw.JavaPratices;

public class OperadoresDeComparacao {
    public static void main(String[] args) {
        // Maior que 
        System.out.println(15>10);

        //Menor que 
        System.out.println(20<15);

        //Igualdade 
        System.out.println(11 == 10);
        
        // Diferenca
        System.out.println(11 != 10);

        // Maior ou igual
        System.out.println(11 >= 10);

        // Menor ou igual
        System.out.println(9 <= 10);

    }
    
}
