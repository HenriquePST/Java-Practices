package IntroducaoJava;

public class OperadoresLogicos {

    public static void main(String[] args) {
        //Not | nao
        System.out.println(!(10 != 11));
        
        //and | E
        System.out.println(10 > 20 && 10 > 5);
        
        // or | Ou
        System.out.println(10 <20 || 10 < 5);
    }
    
}
