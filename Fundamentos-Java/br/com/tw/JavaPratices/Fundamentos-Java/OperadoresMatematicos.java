package br.com.tw.JavaPratices;

public class OperadoresMatematicos {

    public static void main(String[] args) {

        // soma
        var soma = 10 + 10;

        System.out.println(soma);

        // Multiplicacao

        var Multiplicacao = 10 * 10;

        System.out.println(Multiplicacao);

        // divisao

        var divisao = 3.0 / 2.0;
        var divisao1 = 3 / 2f; 

        System.out.println(divisao);
        System.out.println(divisao1);

        //Modulo ( resto de uma divisao)

        var modulo = 3%2;
        var modulo1 = 3%2f;

        System.out.println(modulo);
        System.out.println(modulo1);
    }
}